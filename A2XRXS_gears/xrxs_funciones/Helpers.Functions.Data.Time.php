// Muestra la hora
function Hora_estandar($Hora){	
	if($Hora!='00:00:00'){
		$date = date_create($Hora);
		return date_format($date, 'H:i');
	}else{
		return 'Sin Hora';
	}
}
/*******************************************************************************************************************/
//Devuelve la hora programada
function Hora_prog($Hora){	
	if($Hora!='00:00:00'){
		return date("H:i", strtotime($Hora));
	}else{
		return 'Sin Hora';
	}	
}

